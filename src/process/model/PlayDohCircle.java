package process.model;

public class PlayDohCircle {
	
	private double size;
	
	public PlayDohCircle() {	
	}
	
	public PlayDohCircle(double size) {
		
		this.size = size;
		
	}
	
	public double getSize() {
		return size;
	}
	
	public String toString() {
		
		return "A class to basically nothing, Size: " + size;
		
	}
	
}
